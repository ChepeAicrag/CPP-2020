#include <iostream>
using namespace std;

int main () {
int op1 = 1;
int op2 = 2;
cout << " Operador 1: " << op1 << endl ;
cout << " Operador 2: " << op2 << endl ;
cout << " Mayor que : " <<( op1 > op2 ) << endl ;
cout << " Menor que : " <<( op1 < op2 ) << endl ;
cout << " Mayor o igual que : " <<( op1 >= op2 ) << endl ;
cout << " Menor o igual que : " <<( op1 <= op2 ) << endl ;
cout << " Igual que : " <<( op1 == op2 ) << endl ;
cout << " Diferente que : " <<( op1 != op2 ) << endl ;
return 0;
}